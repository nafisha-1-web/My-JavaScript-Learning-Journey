let count = 0;
count < 10 ;
count++ ;
console.log("Done");

// // Break :
// 1. Exists the loop entirely when needed.
// 2.Remaining Iteration never run.
// 3. Use it to stop as soon as we find needed.

// // Continue :
// 1.Skips the only current Iteration.
// 2.The loop keeps running afterwards.
// 3.Use it to skip the value.



// While Loop VS Do While Loop 


// | `while`                                            | `do...while`                                               |
// |                                                    |                                                                                                              |
// | Checks the condition **before** executing the code | Executes the code **before** checking the condition        |
// | May execute **0 times** if the condition is false  | Executes **at least once**, even if the condition is false |
// | `while (condition) { ... }`                        | `do { ... } while (condition);`                            |


// Easy way to remember :

// while → Check first, then run.
// do...while → Run first, then check.